public class MyCharacter {
    private String color;
    private int eyeNumber;
    private int lifesNumber;
    public void setColor(String color){
        this.color=color;
    }
    public String getColor(){
        return color;
    }

    public int getEyeNumber() {
        return eyeNumber;
    }

    public int getLifesNumber() {
        return lifesNumber;
    }

    public void setEyeNumber(int eyeNumber) {
        this.eyeNumber = eyeNumber;
    }

    public void setLifesNumber(int lifesNumber) {
        this.lifesNumber = lifesNumber;
    }
}
